def set_locale(locale: str) -> None:
    global _LOCALE
    _LOCALE = locale
    for callback in _ON_LOCALE_CHANGED_CALLBACKS:
        try:
            callback()
        except Exception:
            pass


def get_locale() -> str:
    return _LOCALE


def on_locale_changed(callback: Callable[[], None]) -> None:
    _ON_LOCALE_CHANGED_CALLBACKS.append(callback)


def translate(locale: str, key: str, **kwargs) -> str:
    translations = _TRANSLATIONS.get(locale, _TRANSLATIONS["zh_CN"])
    text = translations.get(key, _TRANSLATIONS["zh_CN"].get(key, key))
    if kwargs:
        text = text.format(**kwargs)
    return text


def t(key: str, **kwargs) -> str:
    return translate(_LOCALE, key, **kwargs)
